let num1 = 10;
let num2 = 5;
let num3;
num3 = num1 + num2;
console.log("Kết quả của phép cộng:", num3);
num3 = num1 - num2;
console.log("Kết quả của phép trừ:", num3);
num3 = num1 * num2;
console.log("Kết quả của phép nhân:", num3);
num3 = num1 / num2;
console.log("Kết quả của phép chia:", num3);
